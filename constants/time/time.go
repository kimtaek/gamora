	ThirtySeconds  = OneSecond * 30

	OneMinute      = 60
	FiveMinutes    = OneMinute * 5
	TenMinutes     = OneMinute * 10
	FifteenMinutes = OneMinute * 15
	ThirtyMinutes  = OneMinute * 30

	OneHour = OneMinute * 60
